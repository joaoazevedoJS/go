package Math

var (
	Number01 float64 = 0.0
	Number02 float64 = 0.0
)

func Sum() float64 {
	return Number01 + Number02
}

func Subtraction() float64 {
	return Number01 - Number02
}

func Division() float64 {
	return Number01 / Number02
}

func Multiplication() float64 {
	return Number01 * Number02
}
